  private static final Pattern EAST_AIRPORT_PTN = Pattern.compile("\\d{4} +(?:SW +)?AIRPORT\\b.*");
  private static final Pattern EAST_MAXFIELD_CREEK_PTN = Pattern.compile("[2-9]\\d{4} MAXFIELD CREEK\\b.*|.*\\bWARD RD\\b.*");

  @Override
  public String adjustMapAddress(String addr, String city, boolean cross) {
    if (city.equalsIgnoreCase("PHILOMATH") || city.equals("ALSEA")) {
      addr = HWY_34_PTN.matcher(addr).replaceAll("ALSEA HWY");
    }
    if (city.equalsIgnoreCase("PHILOMATH") || city.equals("BLODGETT")) {
      Matcher match = NUM_HWY_20_PTN.matcher(addr);
      if (!match.matches() || Integer.parseInt(match.group(1)) < 21800) {
        addr = HWY_20_PTN.matcher(addr).replaceAll("CORVALLIS-NEWPORT HWY");
      }
    }

    addr = addr.replace("SW PHILOMATH BLVD", "PHILOMATH BLVD");
    return super.adjustMapAddress(addr);
  }
  private static final Pattern HWY_34_PTN = Pattern.compile("\\bHWY +34\\b", Pattern.CASE_INSENSITIVE);
  private static final Pattern NUM_HWY_20_PTN = Pattern.compile("(\\d+) +HWY +20");
  private static final Pattern HWY_20_PTN = Pattern.compile("\\bHWY +20\\b", Pattern.CASE_INSENSITIVE);

  @Override
  public String adjustMapCity(String city) {
    if (city.equalsIgnoreCase("KINGS VAL")) city = "PHILOMATH";
    return city;
  }

  private static final Pattern STREET_APT_PTN = Pattern.compile("(\\d+)-(\\d+)( +.*)");

  @Override
  protected String adjustGpsLookupAddress(String address, String apt, String place, String city) {
    address = address.toUpperCase().replace("& MP", "MP");
    if (address.contains(" MP ")) {
      address = address.replace("EDDYVILLE-BLODGETT HWY ", "HWY 180 ")
                       .replace("TERRITORIAL RD ",         "HWY 200 ")
                       .replace("KINGS VALLEY HWY ",       "HWY 223 ")
                       .replace("ALSEA-DEADWOOOD HWY ",    "HWY 501 ")
                       .replace(" AND MP ", " MP ");
      return address;
    }

    //  Some common street names in Philomath are duplicated elsewhere :(
    if (address.contains("MAIN ST") || address.contains("N 7TH ST") ||
        address.contains("N 8TH ST")) {
      if (!city.equals("PHILOMATH")) return null;
      apt = "";
    }

    if (apt.length() == 0) {
      Matcher match = STREET_APT_PTN.matcher(address);
      if (match.matches()) {
        address = match.group(1) + match.group(3);
        apt = match.group(2);
      }
    }
    address = address.replace("24671 STOVALL LN", "24617 STOVALL LN");
    if (apt.length() > 0) {
      apt = apt.toUpperCase();
      if (apt.startsWith("LOT")) apt = apt.substring(3).trim();
      address = address + " APT:" + apt;
      return address;
    }

    else return address;
  }

